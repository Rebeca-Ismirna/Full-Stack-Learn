// pop() -> remove the last element of an array

const nomes = ['Rebeca', 'Ismirna', 'Navegantes', 'de', 'Melo']
const nomeremovido = nomes.pop()

console.log(nomes, nomeremovido)

// push() -> add an element at the last index of an array

const numeros = [1, 2, 3, 4, 5, 6, 7, 8]
const addNumber = numeros.push(9)

console.log(numeros, addNumber)